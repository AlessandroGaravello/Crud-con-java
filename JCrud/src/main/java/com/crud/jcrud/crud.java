    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        String id_alumno = jTextField2.getText();
        String curso = jTextField3.getText();

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/java","root","");
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO alumnos (nombre, id_alumno, curso) VALUES (?, ?, ?)");
            stmt.setString(1, nombre);
            stmt.setString(2, id_alumno);
            stmt.setString(3, curso);
            stmt.executeUpdate();
            conn.close();
            JOptionPane.showMessageDialog(null, "Los datos se han aÃ±adido correctamente.");
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al aÃ±adir los datos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = jTextField4.getText();
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/java","root","");
            PreparedStatement stmt = conn.prepareStatement("DELETE FROM alumnos WHERE id = ?");
            stmt.setString(1, id);
            int rowsDeleted = stmt.executeUpdate();
            conn.close();
            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(null, "El registro ha sido eliminado correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "No se ha encontrado ningÃºn registro con el ID proporcionado.");
            }
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al intentar eliminar el registro.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

